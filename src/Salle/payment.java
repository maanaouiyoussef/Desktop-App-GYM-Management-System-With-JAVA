    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String id=jTextField1.getText(); 
            Connection con = dbGeneration.mysql_Connection(); 
            Statement stmt = con.createStatement();
            ResultSet res=stmt.executeQuery("select * from membre WHERE id="+id);
        
            if(res.next()) {
                if(!res.getBoolean("checkPay")) {
                jTextField1.setEditable(false);
                jTextField2.setText(res.getString(9));
                jTextField3.setText(res.getString(3));
                jTextField4.setText(res.getString(4));
                jTextField5.setText(res.getString(5));
                jTextField6.setText(res.getString(12));
                int total = Integer.parseInt(res.getString(12)); 
                String cin = res.getString("CIN");
                TOTAL = new Integer(total);
                ID_CIN = cin;
                PAYEMENT_PROCESS = true;
                } else {
                    JOptionPane.showConfirmDialog(null, "Le Membre a deja pay√© les frais pour ce mois !","Id Invalid",JOptionPane.CLOSED_OPTION);
                    jButton3.setVisible(false);
                    jButton5.setVisible(false);
                    jTextField1.setEditable(false);
                }
            }
            else {
               JOptionPane.showConfirmDialog(null, "Entrer un identifiant qui existe !","Id Invalid",JOptionPane.OK_CANCEL_OPTION);
            }
            disableEditFields();
            
            if(con!=null) {
                if(stmt!=null) {
                    con.close();
                    stmt.close();
                }
            }
            
        } catch(Exception e) {
            System.out.println(e.getMessage());
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

