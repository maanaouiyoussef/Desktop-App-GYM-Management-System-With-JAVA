    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String oldPIN = jPasswordField1.getText();
        String newPIN = jPasswordField2.getText();
        if(oldPIN.equals(currentAccount.getPIN())) {
           try { 
            Connection con = dbGeneration.mysql_Connection();
            Statement stmt = con.createStatement();
            stmt.executeUpdate("update bankaccount set bankPIN='"+newPIN+"' where cin like '%"+currentAccount.getCIN()+"%'");
            stmt.executeUpdate("update membre set bankPIN='"+newPIN+"' where cin like '%"+currentAccount.getCIN()+"%'");
            currentAccount.setPin(newPIN);
            int response = JOptionPane.showConfirmDialog(null, "PIN modifi√© avec succes","",JOptionPane.CLOSED_OPTION);
            
          // close the connection 
            if(con!=null) {
                if(stmt!=null) {
                    stmt.close(); 
                    con.close(); 
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(solde.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else {
            jLabel9.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

